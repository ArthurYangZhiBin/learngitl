public class BackToWaveList extends com.epiphany.shr.ui.action.ActionExtensionBase{
	protected static ILoggerCategory _log = LoggerFactory.getInstance(BackToWaveList.class);

	/**
	 * The code within the execute method will be run from a UIAction specified in metadata.
	 * <P>
	 * @param context The ActionContext for this extension
	 * @param result The ActionResult for this extension (contains the focus and perspective for this UI Extension)
	 *
	 * @return int RET_CONTINUE, RET_CANCEL, RET_CANCEL_EXTENSIONS
	 *
	 * @exception EpiException
	 */
	protected int execute(ActionContext context, ActionResult result) throws EpiException
	{

		StateInterface state = context.getState();

		UnitOfWorkBean uow = state.getDefaultUnitOfWork();
		RuntimeFormInterface toolbar = state.getCurrentRuntimeForm();
		RuntimeFormInterface tabShell = toolbar.getParentForm(state);
		String slotName = tabShell.getSlot().getName();
		
		if("slot_1".equalsIgnoreCase(slotName)){
			context.setNavigation("clickEvent2112");
		}else{
			context.setNavigation("clickEvent1612");
		}
    	Query qry = new Query("wm_wp_wave",null, "wm_wp_wave.WAVEKEY DESC");			 		
		BioCollectionBean resultCollection = uow.getBioCollectionBean(qry);	
		uow.clearState();
		result.setFocus(resultCollection);

		return RET_CONTINUE;
	}
}
