		if (focus instanceof BioCollectionBean) {
			BioCollectionBean focusCollection = (BioCollectionBean) focus;
			for (int i = 0; i < focusCollection.size(); i++) {
				BioBean bioBean = focusCollection.get("" + i);
				_log.debug("LOG_INFO_EXTENSION_AuditDummy_execute", bioBean.getValue("BIOID") + " " + bioBean.getValue("DOAUDIT"), SuggestedCategory.NONE);
			}
		}

