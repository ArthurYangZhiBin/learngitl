public class CodesPreDelete extends
		com.epiphany.shr.ui.action.ActionExtensionBase {

	/**
	 * The code within the execute method will be run from a UIAction specified
	 * in metadata.
	 * <P>
	 * 
	 * @param context
	 *            The ActionContext for this extension
	 * @param result
	 *            The ActionResult for this extension (contains the focus and
	 *            perspective for this UI Extension)
	 * @return int RET_CONTINUE, RET_CANCEL, RET_CANCEL_EXTENSIONS
	 * @throws EpiException
	 *             the epi exception
	 */
	protected int execute(ActionContext context, ActionResult result)
			throws EpiException {

		StateInterface state = context.getState();
		RuntimeFormInterface currentRuntimeForm = state.getCurrentRuntimeForm();
		RuntimeFormInterface listForm = FormUtil.findForm(currentRuntimeForm,
				"", "wm_codesdetail_list_view", state);
		ArrayList<BioBean> selectedItems = ((RuntimeListFormInterface) listForm)
				.getAllSelectedItems();
		for (BioBean item : selectedItems) {
			String editable = BioAttributeUtil.getString(item, "EDITABLE");
			if ("0".equals(editable)) {
				throw new UserException(
						"WMEXP_CODE_DELETE_EDITABLE",
						new String[] { BioAttributeUtil.getString(item, "CODE") });
			}
		}

		// Replace the following line with your code,
		// returning RET_CONTINUE, RET_CANCEL, or RET_CANCEL_EXTENSIONS
		// as appropriate

		return RET_CONTINUE;
	}

}
